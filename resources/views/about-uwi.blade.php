                   <p>The University of the West Indies (UWI), which is situated at Mona, Jamaica, is a prestigious university, which is highly ranked by international world ranking bodies. It is considered to be one of the best universities in the Caribbean. Moreover, UWI has been reputed to have one of the best medical schools in the world. The university has also produced four (4) notable Nobel Laureates, and has as alumni Presidents of various countries within the region. The university comprises of five (5) vibrant faculties, offering a wide range of accredited programmes at Undergraduate, Master’s and Doctoral levels. Its faculties are run by highly trained and competent teachers and researchers, drawn from local, regional and international sources.</p>
                   <p>The University of the West Indies (UWI) Mona campus offers world class, accredited higher education programmes to Jamaica, the region and the globe. UWI engages in research and development designed to support the social and economic growth of the Caribbean region and the world, provide community service, and enrich the academic experience.</p>

