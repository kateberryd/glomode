                  <p>Mona, Jamaica is the founding campus of the unique, multi-campus University of the West Indies. The square mile site welcomed its first undergraduates – 33 medical students from across the West Indies in October 1948, under the British Commonwealth which also established the University of Ibadan the same year. Medicine was added to Natural Science in 1949, Arts in 1950 and, gradually, the full range of the modern university.</p>
                    <p>Today, UWI is the region’s premier educational institution, with faculties offering a wide range of Undergraduate, Master’s and Doctoral programmes in Humanities, Education, Science and Technology, Agriculture, Engineering, Law, Medical Sciences and Social Sciences. Amid the concerns of the present, the past remains visible across the Mona Campus where a number of heritage signs and monuments call attention to the material remnants of the site’s varied history. These include cut stone Roman style aqueducts, an 18th century building re- purposed as a Chapel and much more.</p>
