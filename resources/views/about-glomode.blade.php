                <div class="col-12 col-md-6 wow fadeInUp" data-wow-delay="400ms">
                    <p>The collaboration between Glomode Edu-Project Services Ltd. and the University of West Indies, Mona (UWI), was borne out of the desire to help address the challenge of inadequate access to university education in the country. Available JAMB statistics indicate that between 2011 and 2016 only 2,674,485 out of 11,703,709 candidates were admitted into tertiary institutions. A total of 1,881,488 candidates competed for 750,000 admission slots into tertiary institutions in 2019. Of these, 1,803,742 were university applicants. This therefore means that a great number of young bright minds waste time and effort seeking admission into higher institutions that are just not available. Many Nigerian students cannot afford or are yet to explore the option of studying abroad. The process of recruiting students into the University of West Indies, Mona, through the Glomode Scholarship Entrance Examination, provides an alternative gateway to quality higher education which can help ameliorate the access challenge and promote national growth through human capacity building, science and technology.</p>
                    <p>The admission into the University of West Indies, Mona (UWI) through the Glomode Scholarship Scheme would entail a Computer Based Test (CBT), Essay Writing and Oral Interview to be conducted annually in selected states of the country (Lagos, Oyo, Abuja, Rivers, Kano, Uyo, Plateau, Taraba and Imo States as pilot states). Other states would be added in subsequent exercises. It is imperative for candidates to note that the Computer Based Test (CBT) is the first stage of the selection process followed by the Essay Writing (second stage) and Oral Interview (final stage). Only those who pass the first stage would be eligible for the second stage and those that pass the second stage would be eligible for the final stage. The exams would be conducted under strict invigilation.  The final selection of successful candidates would be done by an UWI team based on the results of candidates from the selection processes. The University of West Indies, Mona Campus, Jamaica, shall facilitate the issuance of admission letters and study visas to successful candidates.</p>
                    
