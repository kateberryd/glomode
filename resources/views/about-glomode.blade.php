                        <h4>Scholarship Package Incentives</h4>

                    <p>The Glomode Scholarship Scheme to the University of West Indies, Mona (UWI) has been designed to be an enduring initiative which would be of great benefit to a myriad of Nigerian youths, their families and the nation. The Scholarship package therefore includes: full payment of tuition, hostel accommodation and feeding, books, and return tickets after study programmes. The scholarships would be awarded to candidates based strictly on MERIT. It is also imperative to note that ALL successful candidates selected for the scholarship award would be required to sign an undertaking of good behaviour and any breach of contract shall result in the immediate termination of offender’s scholarship status and deportation  to Nigeria.</p>
                        <h4>Scholarship Application Requirement</h4>
                
                    <p>Application criteria shall be restricted to O’Levels with five credits in relevant subjects (including Mathematics and English) at no more than two sittings for secondary school leavers and a good CGPA of 3.5 and above out of 5.0 for undergraduate applicants (100L, 200L and 300L). The scholarship application process into UWI Mona will be limited to Nigerian applicants only (for now).</p>
                    <h5 class="mb-4">Applicants must not be more than 25 years of age by December, 2020.</h5>
                    <p><strong>Note:</strong> Undergraduate applicants who do not have the required grade point of 3.5 CGPA for transfer of credits to University of West Indies (UWI) through the Glomode Scholarship Entrance Examination have the opportunity of applying for the scholarship with the required application criteria of five (5) credits in their O’Levels (including Mathematics and English). </p>
